const ua = document.getElementById('ua');
const en = document.getElementById('en');

let language = 1;
let aboutArray;
let boxnumb = 0;

let about = document.getElementById('about');
let tri = document.getElementById('tri');

let activeVersion = 1;
    
let versionElements = document.querySelectorAll('.version');
        
versionElements.forEach((versionElement, index) => {
    versionElement.addEventListener('click', function() {
        versionElements.forEach(el => {
            el.classList.remove('activate');
            el.classList.remove('animate-in');
            el.classList.add('animate-out');
            setTimeout(() => {
                el.classList.remove('animate-out');
                }, 300); 
            });
                
        versionElement.classList.add('activate');
        versionElement.classList.add('animate-in');
        setTimeout(() => {
            versionElement.classList.remove('animate-in');
        }, 300); 

        activeVersion = index + 1;
        console.log("Активована версія: " + activeVersion);
        drawVersion();
        video();
    });
});

function video(){
    let videos = document.querySelectorAll('.video-container');
    videos.forEach(video => {
        video.style.display = "none";
    });

    let activeVideo = document.getElementById(`video${activeVersion}`);
    if (activeVideo) {
        activeVideo.style.display = "block";
    }
}

video();

function aboutLanguage(boxNumb){           
    boxnumb = boxNumb;
    tri.style.setProperty('display', 'block');
    about.style.setProperty('display', 'block');
    switch (boxNumb) {
        case 1:
            tri.style.setProperty('margin-left','10%');
            about.innerText = aboutArray[0];
            break;
        case 2: 
            tri.style.setProperty('margin-left','30%');
            about.innerText = aboutArray[1];
            break;
        case 3: 
            tri.style.setProperty('margin-left','48%');
            about.innerText = aboutArray[2];
            break;
        case 4: 
            tri.style.setProperty('margin-left','68%');
            about.innerText = aboutArray[3];
            break;
        default:
            tri.style.setProperty('display', 'none');
            about.style.setProperty('display', 'none')
            break;
    }
}

function changeLanguage(languageNumb) {
    language = languageNumb;
    let text = language === 0 ? ukText : enText;

    document.getElementById("title").innerText = text.title;
    document.getElementById("name").innerText = text.name;
    document.getElementById("profession").innerText = text.profession;
    document.getElementById("city").innerText = text.city;
    document.getElementById("education").innerText = text.education;
    document.getElementById("language").innerText = text.languages;
    document.getElementById("ukrainian").innerText = text.ukrainian;
    document.getElementById("english").innerText = text.english;
    document.getElementById("programming-languages").innerText = text.programmingLanguages;
    document.getElementById("title-technologies").innerText = text.titleTechnologies;
    document.getElementById("title-education").innerText = text.titleEducation;
    document.getElementById("experience").innerText = text.experience;
    document.getElementById("about-me").innerText = text.aboutMe;
    
    document.getElementById("text-about").innerHTML = text.textAbout;

    for(i=0; i<3; i++){
        document.getElementById(`version${i+1}`).innerText = text.version[i];
    }
    aboutArray = text.aboutLanguage;

    if(language===0){
        ua.style.setProperty('color','var(--extra-color)');
        en.style.setProperty('color','var(--text-color)')
    }else{
        en.style.setProperty('color','var(--extra-color)');
        ua.style.setProperty('color','var(--text-color)')
    }
    aboutLanguage(boxnumb);
    drawVersion();
}

changeLanguage(language);


function hide(){
    about.style.setProperty('display', 'none');
    tri.style.setProperty('display', 'none');
}

function drawVersion(){
    let text = language === 0 ? ukText : enText;
    document.getElementById("work-purpose").innerText = text.keyWords[0];
    document.getElementById("done").innerText = text.keyWords[1];

    document.getElementById("purpose").innerText = text.versionDescription[activeVersion-1][0];
    let list = document.getElementById("to-do-list");
    list.innerHTML = '';
    text
        .versionDescription[activeVersion-1]
        .slice(1)
        .forEach(desc => {
            const li = document.createElement('li');
            li.textContent = desc;
            list.appendChild(li);
        })
}

drawVersion()